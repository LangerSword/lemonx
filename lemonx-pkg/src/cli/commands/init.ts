function parseArgs(args: string[]) {
  let targetDir = process.cwd();
  let resourceClass = "";
  let runnerType: RunnerType = "container";

  for (let i = 0; i < args.length; i++) {
    if (args[i] === "--runner" && args[i + 1]) {
      resourceClass = args[++i];
    } else if (args[i] === "--type" && args[i + 1]) {
      const type = args[++i];
      if (type === "machine") {
        runnerType = "machine";
      } else if (type === "container") {
        runnerType = "container";
      } else {
        console.error(`❌ Invalid runner type: "${type}". Use "container" or "machine".`);
        process.exit(1);
      }
    } else if (!args[i].startsWith("--")) {
      targetDir = args[i];
    }
  }

  return { targetDir, resourceClass, runnerType };
}

export async function init(args: string[] = []) {
  const { targetDir, resourceClass, runnerType } = parseArgs(args);
  const circleciDir = join(targetDir, ".circleci");

  console.log("🍋 lemonx init — setting up CircleCI AI test integration\n");

  if (!existsSync(targetDir)) {
    console.error(`❌ Directory not found: ${targetDir}`);
    process.exit(1);
  }

  if (!resourceClass) {
    console.error("❌ Missing required --runner flag.");
    console.error("   Usage: npx lemonx init [dir] --runner <namespace>/<resource-class>");
    console.error("   Example: npx lemonx init ./my-repo --runner my-org/lemon-runner");
    process.exit(1);
  }

  if (!resourceClass.includes("/")) {
    console.error(`❌ Invalid runner resource class: "${resourceClass}"`);
    console.error("   Expected format: <namespace>/<resource-class>");
    console.error("   Example: my-org/lemon-runner");
    process.exit(1);
  }

  await mkdir(circleciDir, { recursive: true });

  const configPath = join(circleciDir, "config.yml");
  const exists = existsSync(configPath);

  if (exists) {
    const existing = await readFile(configPath, "utf-8");
    if (existing.includes("lemonx") || existing.includes("resource_class")) {
      console.log("⚠️  CircleCI config already contains lemonx integration. Skipping.");
      console.log(`   Config: ${configPath}`);
      console.log(`   Runner: ${resourceClass} (${runnerType})`);
      return;
    }
    console.log(`⚠️  Existing CircleCI config found at: ${configPath}`);
    console.log("   Back it up before overwriting.\n");
  }

  const config = runnerType === "container"
    ? generateContainerConfig(resourceClass)
    : generateMachineConfig(resourceClass);

  await writeFile(configPath, config, "utf-8");
  console.log(`✅ CircleCI config written to: ${configPath}`);
  console.log(`   Runner: ${resourceClass} (${runnerType})`);

  console.log("\nNext steps:");
  if (runnerType === "container") {
    console.log("1. Push to any branch (not main) and CircleCI will trigger the AI test loop!");
  } else {
    console.log("1. Start the lemon.test machine runner:");
    console.log("   docker compose -f docker-compose.runner.yml up -d");
    console.log("\n2. Push to any branch (not main) and CircleCI will trigger the AI test loop!");
  }
}
