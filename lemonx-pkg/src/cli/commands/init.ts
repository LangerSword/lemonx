import { mkdir, writeFile, readFile } from "fs/promises";
import { join } from "path";
import { existsSync } from "fs";

type RunnerType = "container" | "machine";

function generateContainerConfig(resourceClass: string) {
  return `# Auto-generated by lemonx — AI-powered test generation, execution, and self-healing fixes
#
# This config runs on a CircleCI container runner (Kubernetes).
# No webhook, no tunnel, no external server needed.

version: 2.1

workflows:
  ai-test-and-fix:
    jobs:
      - ai-test-loop:
          filters:
            branches:
              ignore: main

jobs:
  ai-test-loop:
    docker:
      - image: cimg/node:22.0
    resource_class: ${resourceClass}
    working_directory: ~/project
    steps:
      - checkout

      - run:
          name: Install lemon.test dependencies
          command: |
            npm install

      - run:
          name: Run lemon.test AI test-fix loop
          command: |
            echo "🍋 Running AI test generation, execution, and self-healing fixes..."
            cd ~/project

            RESPONSE=$(npx tsx src/index.ts 2>&1)

            echo "$RESPONSE"

            if echo "$RESPONSE" | grep -q "⚠️.*Max iterations reached"; then
              echo "⚠️  AI could not fix all tests within the iteration limit"
              exit 1
            fi

            if echo "$RESPONSE" | grep -q "❌"; then
              echo "❌ AI test loop failed"
              exit 1
            fi

            echo "✅ All tests passed"

  ai-generate-tests:
    docker:
      - image: cimg/node:22.0
    resource_class: ${resourceClass}
    working_directory: ~/project
    steps:
      - checkout

      - run:
          name: Install dependencies
          command: npm install

      - run:
          name: Generate AI tests
          command: |
            echo "🍋 Generating tests..."
            npx tsx src/index.ts --generate-only

  ai-run-tests:
    docker:
      - image: cimg/node:22.0
    resource_class: ${resourceClass}
    working_directory: ~/project
    steps:
      - checkout

      - run:
          name: Install dependencies
          command: npm install

      - run:
          name: Run existing tests
          command: |
            echo "🍋 Running tests..."
            npx vitest run --reporter=verbose
`;
}

